# Program to demonstrate if-else conditional statements in Python
# If block of code will run only if the condition is true otherwise else block of code will run

number = 10

if number > 0:
    print("The number is positive")
else:
    print("The number is not positive")

# Example with user input
age = int(input("Enter your age: "))

if age >= 18:
    print("You are eligible to vote")
else:
    print("You are not eligible to vote")
