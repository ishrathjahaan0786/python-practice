# Program to demonstrate type conversion in Python

age_str = "20"
age = int(age_str)

height = 5.8
height_int = int(height)

number = 10
number_str = str(number)
num = float(number)

print("Age:", age)
print("Height (int):", height_int)
print("Number as string:", number_str)
print("Number as float:", num)
